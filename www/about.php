	<p>Alisohn was born in the mid-sixties in London England at a time when single parenting was less common in comparison with today's society.  There was very little support to assist families in need like there is today.  Her mother struggled to raise three children on her own and faced her own daily battles.
Alisohn describes what her life was like growing up without knowing her father and the constant reminder that she continues to deal with throughout her life.
 </p>
 <p>
Alisohn left UK in1997 and now resides in New Zealand with her daughter.
After many attempts to find her father her search was futile until in 2011 she discovered she had inherited a blood disease passed on through her family genes.  With todays technology it took Alisohn three months to trace the family she never knew and shares her journey of disappointments and triumphs.
 </p>
 <p>
Alisohn shares from her perspective her personal experience, in the hope that sharing this insight with mothers and fathers, may enlighten parents to both share their role and responsibilities to their children.  She explains how the impact of our decisions can influence our children's lives. 
 </p>
 <p>
The journey to find her family took Alisohn on a path well travelled by many who had searched and found members of their family  Alisohn wants to share her knowledge, which may help someone needing to trace their family.  She wants to encourage you to never give up hope.  It was one encounter with hope 25 years later that she discovered the truth.
 </p>
